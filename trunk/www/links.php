<h3>Related</h3>

<a href="http://citybuilder.sourceforge.net/">Andrew Sampson's CityBuilder</a>
<p>Andrew spent quite a bit of time decoding the Syndicate data files - most of
what we currently know about them is due to his work. You can find his Syndicate
data file utilities on his CityBuilder page.</p>

<a href="http://www.yoda.arachsys.com/dk/utils.html">Dungeon Keeper Utilities</a>
<p>Bullfrog used similar formats for several data files throughout their early
game releases, such as Syndicate, Syndicate Wars, Dungeon Keeper and Magic
Carpet. Specifically, the compression format that they used for each of these
games was the same (RNC Compression). You can find several tools that are
relevant to decoding the Syndicate data files at the Dungeon Keeper Utilities
page, most notably a RNC decompressor.</p>

<a href="http://www.ea.com/">Electronic Arts</a>
<p>EA currently holds the copyright of both the Bullfrog name and Syndicate
itself. They were the worldwide distributors of Bullfrog games, while Bullfrog
was still a mildly independant game studio.</p>

